    <script>

        $("#contact_form").on("submit", function (e) {
        $('#contact_form button[type="submit"]').prop('disabled', true);
        $('#contact_form button[type="submit"]').text('GÖNDERİLİYOR...');

        e.preventDefault();
        $.ajax({
            type: 'POST',
            url: 'https://api.kodlanabilir.com/mailer.php',
            data: $(this).serialize(), // Serialize the data from the form
            success: function(response) {
                if (response.trim() == "ok") {
                    alert('Your message has been received! We will get back to you as soon as possible. Thank you!');
                    $('#contact_form')[0].reset();
                } else {
                    alert("Your request is currently being processed. Please try again later.")
                }
            },
            error: function(response) {
                alert("Your request is currently being processed. Please try again later.")
            },
            complete: function() {
                // Re-enable the button when the AJAX request is completed
                $('#contact_form button[type="submit"]').prop('disabled', false);
                $('#contact_form button[type="submit"]').text('GÖNDER');
            }
        });
        });

</script>	
